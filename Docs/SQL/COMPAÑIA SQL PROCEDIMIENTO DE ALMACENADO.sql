-- LISTAR REGISTROS POR SUCURSAL --
CREATE PROCEDURE SP_L_COMPANIA_01
AS
BEGIN

	SELECT * FROM TM_COMPANIA
	WHERE ACTIVE = 1
END


-- OBETENER REGISTROS POR ID --
CREATE PROCEDURE SP_L_COMPANIA_02
 @COM_ID INT
AS
BEGIN

	SELECT * FROM TM_COMPANIA
	WHERE COM_ID = @COM_ID
END

-- CAMBIAR ESTADO DE LA COMPANIA (DELETE)--
CREATE PROCEDURE SP_D_COMPANIA_01
@COM_ID INT
AS
BEGIN

	UPDATE TM_COMPANIA
	SET
		ACTIVE = 0
	WHERE 
		COM_ID = @COM_ID
	
END
-- INSERTAR REGISTROS --
CREATE PROCEDURE SP_I_COMPANIA_01
@COM_NAME VARCHAR(150)
AS
BEGIN

	INSERT INTO TM_COMPANIA
		(COM_NAME, CREATED_AT, ACTIVE)
	VALUES
		(@COM_NAME, GETDATE(), 1)
	
END

-- ACTUALIZAR REGISTROS --
CREATE PROCEDURE SP_U_COMPANIA_01
@COM_ID INT,
@COM_NAME VARCHAR(150)
AS
BEGIN

	UPDATE TM_COMPANIA
	SET
		COM_NAME = @COM_NAME
	WHERE 
		COM_ID = @COM_ID
	
END