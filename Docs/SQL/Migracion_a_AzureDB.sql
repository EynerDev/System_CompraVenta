CREATE PROCEDURE [dbo].[SP_L_CATEGORIA_STOCK]
@SUC_ID INT
AS 
BEGIN
	SELECT 
	TM_CATEGORIA.CAT_ID, 
	TM_CATEGORIA.CAT_NAME, 
	SUM(TM_PRODUCTO.PROD_STOCK) AS STOCK_TOTAL
	FROM     
	TM_CATEGORIA INNER JOIN
		   TM_PRODUCTO ON TM_CATEGORIA.CAT_ID = TM_PRODUCTO.CAT_ID
	WHERE  
	TM_CATEGORIA.SUC_ID = @SUC_ID AND
	TM_PRODUCTO.ACTIVE = 1 AND 
	TM_CATEGORIA.ACTIVE = 1
	GROUP BY
	TM_CATEGORIA.CAT_ID,
	TM_CATEGORIA.CAT_NAME
	ORDER BY STOCK_TOTAL DESC
END
