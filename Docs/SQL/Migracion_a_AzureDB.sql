CREATE PROCEDURE [dbo].[SP_CONSUMO_COMPRAS_DIARIAS]
@SUC_ID INT 
AS
BEGIN
	SELECT 
	CONVERT(VARCHAR, CREATED_AT, 103) AS FECHA,
	SUM(COMPRA_TOTAL) AS TOTAL
	FROM TM_COMPRA
