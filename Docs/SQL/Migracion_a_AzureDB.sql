CREATE PROCEDURE [dbo].[SP_L_TOP_PRODUCTOS_01]
    @SUC_ID INT
AS
BEGIN
    -- Selecciona los 5 productos principales por cantidad comprada para una sucursal específica
    SELECT TOP (5) 
        TD_COMPRA_DETALLE.PROD_ID, 
        FORMAT(SUM(TM_COMPRA.COMPRA_TOTAL), 'N2') AS TOTAL, 
        SUM(TD_COMPRA_DETALLE.DET_CANT) AS CANT, 
        TM_PRODUCTO.PROD_NAME, 
        TM_CATEGORIA.CAT_NAME, 
        TM_UNIDAD_DE_MEDIDA.UNID_NAME, 
        FORMAT(TM_PRODUCTO.PROD_PCOMPRA, 'N2') AS PRECIO_PRODUCTO, 
        TM_PRODUCTO.PROD_STOCK, 
        TM_PRODUCTO.PROD_IMG, 
        TM_MONEDA.MON_NAME
    FROM     
        TD_COMPRA_DETALLE 
    INNER JOIN
        TM_PRODUCTO ON TD_COMPRA_DETALLE.PROD_ID = TM_PRODUCTO.PROD_ID 
    INNER JOIN
        TM_UNIDAD_DE_MEDIDA ON TM_PRODUCTO.UNID_ID = TM_UNIDAD_DE_MEDIDA.UNI_ID 
    INNER JOIN
        TM_CATEGORIA ON TM_PRODUCTO.CAT_ID = TM_CATEGORIA.CAT_ID 
    INNER JOIN
        TM_COMPRA ON TD_COMPRA_DETALLE.COMPRA_ID = TM_COMPRA.COMPRA_ID 
    INNER JOIN
        TM_MONEDA ON TM_PRODUCTO.MON_ID = TM_MONEDA.MON_ID 
                   AND TM_COMPRA.MON_ID = TM_MONEDA.MON_ID
    WHERE  
        TM_COMPRA.ACTIVE = 1 
        AND TM_PRODUCTO.ACTIVE = 1 
        AND TM_COMPRA.SUC_ID = @SUC_ID
    GROUP BY 
        TD_COMPRA_DETALLE.PROD_ID, 
        TM_PRODUCTO.PROD_NAME, 
        TM_CATEGORIA.CAT_NAME, 
        TM_UNIDAD_DE_MEDIDA.UNID_NAME, 
        TM_PRODUCTO.PROD_PCOMPRA, 
        TM_PRODUCTO.PROD_STOCK, 
        TM_PRODUCTO.PROD_IMG, 
        TM_MONEDA.MON_NAME
    ORDER BY 
        CANT DESC
END
