
CREATE PROCEDURE SP_U_MENU_01
@MEND_ID INT
AS
BEGIN
	UPDATE TD_MENU
	SET 
		MEND_PERMISO = 'SI'
		WHERE MEND_ID = @MEND_ID

END

CREATE PROCEDURE SP_U_MENU_02
@MEND_ID INT
AS
BEGIN
	UPDATE TD_MENU
	SET 
		MEND_PERMISO = 'NO'
		WHERE MEND_ID = @MEND_ID

END
SELECT * FROM TD_MENU
--LISTAR REGISTROS
CREATE PROCEDURE SP_LISTAR_MENU_01     
@ROLE_ID INT    
AS    
BEGIN    
 SELECT TD_MENU.*, TM_MENU.MEN_NAME, TM_MENU.MEND_GROUP, TM_MENU.MEN_RUTA, TM_MENU.MEN_IDENTI    
 FROM     TD_MENU INNER JOIN    
       TM_MENU ON TD_MENU.MEN_ID = TM_MENU.MEN_ID    
 WHERE     
 TD_MENU.ROLE_ID = @ROLE_ID    
END