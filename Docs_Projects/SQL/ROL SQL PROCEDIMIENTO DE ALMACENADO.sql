-- LISTAR REGISTROS POR SUCURSAL --
CREATE PROCEDURE SP_L_ROL_01
@SUC_ID INT
AS
BEGIN

	SELECT * FROM TM_ROL
	WHERE SUC_ID = @SUC_ID
END


-- OBETENER REGISTROS POR ID --
CREATE PROCEDURE SP_L_ROL_02
 @ROLE_ID INT
AS
BEGIN

	SELECT * FROM TM_ROL
	WHERE ROLE_ID = @ROLE_ID
END

-- CAMBIAR ESTADO DE ROL (DELETE)--
CREATE PROCEDURE SP_D_ROL_01
@ROLE_ID INT
AS
BEGIN

	UPDATE TM_ROL
	SET
		ACTIVE = 0
	WHERE 
		ROLE_ID = @ROLE_ID
	
END
-- INSERTAR REGISTROS --
CREATE PROCEDURE SP_I_ROL_01
@SUC_ID INT,
@UNI_NAME VARCHAR(150)
AS
BEGIN

	INSERT INTO TM_ROL
		(SUC_ID, ROLE_NAME, CREATED_AT, ACTIVE)
	VALUES
		(@SUC_ID, @UNI_NAME, GETDATE(), 1)
	
END

-- ACTUALIZAR REGISTROS --
CREATE PROCEDURE SP_U_ROL_01
@ROLE_ID INT,
@SUC_ID INT,
@ROLE_NAME VARCHAR(150)
AS
BEGIN

	UPDATE TM_ROL
	SET
		SUC_ID = @SUC_ID,
		ROLE_NAME = @ROLE_NAME
	WHERE 
		ROLE_ID = @ROLE_ID
	
END