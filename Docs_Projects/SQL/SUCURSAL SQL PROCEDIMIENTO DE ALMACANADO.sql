-- LISTAR REGISTROS POR EMPRESA --
CREATE PROCEDURE SP_L_SUCURSAL_01
@EMP_ID INT
AS
BEGIN

	SELECT * FROM TM_SUCURSAL
	WHERE EMP_ID = @EMP_ID
END


-- OBETENER REGISTROS POR ID --
CREATE PROCEDURE SP_L_SUCURSAL_02
 @SUC_ID INT
AS
BEGIN

	SELECT * FROM TM_SUCURSAL
	WHERE 
	SUC_ID = @SUC_ID 
	AND ACTIVE = 1
END

-- CAMBIAR ESTADO DE LA  UNIDAD DE MEDIDA (DELETE)--
CREATE PROCEDURE SP_D_SUCURSAL_01
@SUC_ID INT
AS
BEGIN

	UPDATE TM_SUCURSAL
	SET
		ACTIVE = 0
	WHERE 
		SUC_ID = @SUC_ID
	
END
-- INSERTAR REGISTROS --
CREATE PROCEDURE SP_I_SUCURSAL_01
@EMP_ID INT,
@SUC_NAME VARCHAR(50)

AS
BEGIN

	INSERT INTO TM_SUCURSAL
		(EMP_ID, SUC_NAME, CREATED_AT, ACTIVE)
	VALUES
		(@EMP_ID, @SUC_NAME, GETDATE(), 1)
	
END

-- ACTUALIZAR REGISTROS --
CREATE PROCEDURE SP_U_SUCURSAL_01
@SUC_ID INT,
@EMP_ID INT,
@SUC_NAME INT

AS
BEGIN

	UPDATE TM_SUCURSAL
	SET
		EMP_ID = @EMP_ID,
		SUC_NAME = @SUC_NAME
	WHERE 
		SUC_ID = @SUC_ID
	
END